
// Perguntar dois números ao usuário
let numero1 = Number(prompt("Primeiro número"))
let numero2 = Number(prompt("Segundo número"))

// Comparar números e imprimir no console
console.log("O primeiro é igual ao segundo", numero1 === numero2)
console.log("O primeiro é diferente do segundo", numero1 !== numero2)
console.log("O primeiro é maior que o segundo", numero1 > numero2)
console.log("O primeiro é menor que o segundo", numero1 < numero2)
