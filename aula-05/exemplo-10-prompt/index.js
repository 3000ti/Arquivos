
// Perguntar nome e idade ao usuário
const nome = prompt("Qual é o seu nome?")
const idade = prompt("Qual é a sua idade?")

// Converter a idade para numeral
const idadeNumero = Number(idade)

// Imprimir variáveis e os seus tipos no console
console.log("Nome do usuário:", nome)
console.log("Tipo da constante nome:", typeof nome)

console.log("Idade do usuário:", idade)
console.log("Tipo da constante idade:", typeof idade)

console.log("Idade do usuário em numerais:", idadeNumero)
console.log("Tipo da constante idade:", typeof idadeNumero)
