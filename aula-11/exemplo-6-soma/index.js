
// Declarar a função somar ()
// - Recebe dois números
// - Retorna o resultado da soma dos números
function somar (a, b) {
    // Retornar a soma dos números
    return a + b
}

// Imprimir o resultado no console
console.log(somar(1, 100))
console.log(somar(10, 10))
console.log(somar(1000, 10))
