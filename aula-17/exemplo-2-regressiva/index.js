
// Iniciar variável para contagem
let contagem = 10

// Mensagem de início
console.log("Contagem regressiva")

// Laço de repetição com contagem regressiva
while (contagem > 0) {
    // Imprimir número no console
    console.log(contagem)

    // Diminuir valor da contagem em 1
    contagem--
}

// Mensagem de finalização
console.log("Lançar")
